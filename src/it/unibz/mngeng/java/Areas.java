package it.unibz.mngeng.java;

public class Areas {
	
	private int id;
	private int fieldId;
	private double moistureMin;
	private int wateringTime;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getFieldId() {
		return fieldId;
	}
	public void setFieldId(int fieldId) {
		this.fieldId = fieldId;
	}
	public double getMoistureMin() {
		return moistureMin;
	}
	public void setMoistureMin(double moistureMin) {
		this.moistureMin = moistureMin;
	}
	public int getWateringTime() {
		return wateringTime;
	}
	public void setWateringTime(int wateringTime) {
		this.wateringTime = wateringTime;
	}
	
}
